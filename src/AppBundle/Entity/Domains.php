<?php

namespace AppBundle\Entity;

/**
 * Domains
 */
class Domains
{
    /**
     * @var string
     */
    private $domain;

    /**
     * @var boolean
     */
    private $toCheck = '0';

    /**
     * @var integer
     */
    private $did;


    /**
     * Set domain
     *
     * @param string $domain
     *
     * @return Domains
     */
    public function setDomain($domain)
    {
        $this->domain = $domain;

        return $this;
    }

    /**
     * Get domain
     *
     * @return string
     */
    public function getDomain()
    {
        return $this->domain;
    }

    /**
     * Set toCheck
     *
     * @param boolean $toCheck
     *
     * @return Domains
     */
    public function setToCheck($toCheck)
    {
        $this->toCheck = $toCheck;

        return $this;
    }

    /**
     * Get toCheck
     *
     * @return boolean
     */
    public function getToCheck()
    {
        return $this->toCheck;
    }

    /**
     * Get did
     *
     * @return integer
     */
    public function getDid()
    {
        return $this->did;
    }
}
