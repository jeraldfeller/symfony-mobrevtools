<?php

namespace AppBundle\Entity;

/**
 * AffiliateNetwork
 */
class AffiliateNetwork
{
    /**
     * @var string
     */
    private $networkName;

    /**
     * @var string
     */
    private $apiKey;

    /**
     * @var integer
     */
    private $affiliateId;

    /**
     * @var integer
     */
    private $affiliateNetworkId;


    /**
     * Set networkName
     *
     * @param string $networkName
     *
     * @return AffiliateNetwork
     */
    public function setNetworkName($networkName)
    {
        $this->networkName = $networkName;

        return $this;
    }

    /**
     * Get networkName
     *
     * @return string
     */
    public function getNetworkName()
    {
        return $this->networkName;
    }

    /**
     * Set apiKey
     *
     * @param string $apiKey
     *
     * @return AffiliateNetwork
     */
    public function setApiKey($apiKey)
    {
        $this->apiKey = $apiKey;

        return $this;
    }

    /**
     * Get apiKey
     *
     * @return string
     */
    public function getApiKey()
    {
        return $this->apiKey;
    }

    /**
     * Set affiliateId
     *
     * @param integer $affiliateId
     *
     * @return AffiliateNetwork
     */
    public function setAffiliateId($affiliateId)
    {
        $this->affiliateId = $affiliateId;

        return $this;
    }

    /**
     * Get affiliateId
     *
     * @return integer
     */
    public function getAffiliateId()
    {
        return $this->affiliateId;
    }

    /**
     * Get affiliateNetworkId
     *
     * @return integer
     */
    public function getAffiliateNetworkId()
    {
        return $this->affiliateNetworkId;
    }
}
