<?php

namespace AppBundle\Entity;

/**
 * ImportGroup
 */
class ImportGroup
{
    /**
     * @var string
     */
    private $description;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set description
     *
     * @param string $description
     *
     * @return ImportGroup
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Get igid
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
}
